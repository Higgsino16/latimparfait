    -- En latin, on répartit les verbes dans 5 groupes que l'on appelle des conjugaisons :<br/>
 1re conjugaison : radical en -a = amo, amas, amare : aimer<br/>
 2e conjugaison : radical en -e = teneo, tenes, tenere : tenir<br/>
 3e conjugaison : radical terminé par une consonne = lego, legis, legere : lire<br/>
 3e conjugaison mixte : intermédiaire entre la 3e (radical terminé par une consonne) et la 4e (1e personne du sing. en -io) = capio, capis, capere<br/>
 4e conjugaison : radical en -i = audio, audis, audire<br/>
 <br/>
 <br/>

-- Dans un dictionnaire latin, les verbes sont toujours présentés de la manière suivante : d'abord, la 1re personne du singulier du présent de l'indicatif, puis la 2e personne du singulier du présent de l'indicatif, et enfin l'infinitif présent. Ces formes sont utiles pour déterminer à quelle conjugaison appartient un verbe.
Comment déterminer un radical ?<br/>
<br/>

-- Cas général :<br/>
On prend l'infinitif du verbe et on enlève la terminaison -re .<br/>
 1re conjugaison : amare = ama|re . Le radical est donc ama- .<br/>
 2e conjugaison : tenere = tene|re . Le radical est donc tene- .<br/>
 4e conjugaison : audire = audi|re . Le radical est donc audi- .<br/>
 <br/>
 <br/>
-- Cas particuliers :<br/>
On prend l'infinitif du verbe et on enlève la terminaison -re . Attention, le radical se termine par une consonne, donc on enlève une voyelle appelée : voyelle thématique .<br/>
3e conjugaison : legere = leg|e|re . Le radical est donc leg- .<br/>
3e conjugaison mixte : capere = cap|e|re . Le radical est donc cap-<br/>
<br/>
Pour la 3e conjugaison et la 3e mixte, le radical se termine par une consonne, que l'on fait suivre d'une 'voyelle-tampon' (-i- de la 2e du sing. à la 2e du pl., -u- à la 3e du pl et -e- à l'infinitif).<br/>
Cette voyelle permet de faciliter la prononciation de la terminaison, en évitant une suite de plusieurs consonnes.<br/>
